"use client";
import { Input } from "@/components/ui/input";
import { Button } from "@/components/ui/button";
import { Avatar, AvatarImage, AvatarFallback } from "@/components/ui/avatar";
import { Card, CardHeader, CardContent } from "@/components/ui/card";
import { useRef, useState } from "react";
import { useMutation } from "@tanstack/react-query";
import postServices from "@/services/post.services";

export function PostComposer() {
  const [caption, setCaption] = useState<string>("");
  const fileInputRef = useRef<HTMLInputElement>(null);

  const PostMutation = useMutation({
    mutationFn: (data: any) => postServices.postData(data),
    onSuccess: (res: any) => {
      console.log(res, "success");
    },
  });
  const handleCaption = (e: React.ChangeEvent<HTMLInputElement>) => {
    setCaption(e.target.value);
  };
  const handleButtonClick = () => {
    if (fileInputRef.current) {
      fileInputRef.current.click();
    }
  };

  const handlePost = () => {
    const data = new FormData();
    data.append("caption", caption);
    if (fileInputRef.current && fileInputRef.current.files) {
      for (let i = 0; i < fileInputRef.current.files.length; i++) {
        data.append("image", fileInputRef.current.files[i]);
      }
    }
    PostMutation.mutate(data);
    console.log(data, "caption");
  };
  console.log(fileInputRef, "fileInputRef");
  return (
    <div className="flex flex-col items-center gap-4 py-6">
      <Card className="w-full max-w-sm bg-background p-4">
        <CardHeader className="flex items-center gap-2">
          <Avatar className="h-10 w-10">
            <AvatarImage src="/placeholder-user.jpg" alt="@shadcn" />
            <AvatarFallback>JD</AvatarFallback>
          </Avatar>
          <Input
            type="text"
            placeholder="What's on your mind?"
            className="flex-1"
            onChange={handleCaption}
          />
        </CardHeader>
        <CardContent className="pt-2">
          <div className="grid grid-cols-4 gap-2">
            <input
              type="file"
              ref={fileInputRef}
              style={{ display: "none" }}
              accept="image/*"
            />
            <Button
              variant="outline"
              size="icon"
              className="p-2"
              onClick={handleButtonClick}
            >
              <ImageIcon className="h-5 w-5" />
              <span className="sr-only">Upload Image</span>
            </Button>
            <Button variant="outline" size="icon" className="p-2">
              <VideoIcon className="h-5 w-5" />
              <span className="sr-only">Upload Video</span>
            </Button>
            <Button variant="outline" size="icon" className="p-2">
              <TextIcon className="h-5 w-5" />
              <span className="sr-only">Create Text Post</span>
            </Button>
            <Button
              className="col-span-1 justify-self-end"
              onClick={handlePost}
            >
              Post
            </Button>
          </div>
        </CardContent>
      </Card>
    </div>
  );
}

